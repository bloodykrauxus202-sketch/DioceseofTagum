// Helper function to extract last name from full priest name
function extractLastName(fullName: string): string {
  // Remove common prefixes
  let name = fullName
    .replace(/^(Rev\.|Rev|Msgr\.|Most Rev\.|Fr\.)\s*/gi, '')
    .replace(/^Fr\.\s*/gi, '')
    .trim();

  // Split by spaces
  const parts = name.split(/\s+/);

  // Filter out suffixes and middle initials
  const suffixes = ['jr', 'jr.', 'sr', 'sr.', 'iii', 'ii', 'csch'];
  const filtered = parts.filter(p => {
    const lower = p.toLowerCase().replace(/,/g, '');
    // Skip single letter initials (like middle initials)
    if (p.length <= 2 && p.includes('.')) return false;
    // Skip suffixes
    if (suffixes.includes(lower)) return false;
    return true;
  });

  // Return the last meaningful part (the surname)
  return filtered[filtered.length - 1]?.toLowerCase().replace(/,/g, '') || '';
}

// Get image URL for a priest by their full name
export function getPriestImageUrl(priestName: string): string | undefined {
  const lastName = extractLastName(priestName);

  // Direct match
  if (priestImageUrls[lastName]) {
    return priestImageUrls[lastName];
  }

  // Try without accents/special characters
  const normalizedLastName = lastName
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/ñ/g, 'n')
    .replace(/Ñ/g, 'n');

  if (priestImageUrls[normalizedLastName]) {
    return priestImageUrls[normalizedLastName];
  }

  // Try partial match for hyphenated or compound names
  for (const [key, url] of Object.entries(priestImageUrls)) {
    if (lastName.includes(key) || key.includes(lastName)) {
      return url;
    }
  }

  return undefined;
}

// Check if a priest has an image
export function hasPriestImage(priestName: string): boolean {
  return getPriestImageUrl(priestName) !== undefined;
}
