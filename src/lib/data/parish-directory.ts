// Get parish by ID
export function getParishById(id: string): Parish | undefined {
  return parishes.find(p => p.id === id);
}

// Get clergy by ID
export function getClergyById(id: string): Clergy | undefined {
  return clergy.find(c => c.id === id);
}

// Filter parishes by type
export function getParishesByType(type: Parish['type']): Parish[] {
  return parishes.filter(p => p.type === type);
}

// Filter parishes by vicariate
export function getParishesByVicariate(vicariate: string): Parish[] {
  return parishes.filter(p => p.vicariate === vicariate);
}

// Filter clergy by category
export function getClergyByCategory(category: Clergy['category']): Clergy[] {
  return clergy.filter(c => c.category === category);
}

// Search parishes
export function searchParishes(query: string): Parish[] {
  const lowerQuery = query.toLowerCase();
  return parishes.filter(p =>
    p.name.toLowerCase().includes(lowerQuery) ||
    p.vicariate.toLowerCase().includes(lowerQuery) ||
    p.location.toLowerCase().includes(lowerQuery) ||
    p.priestName.toLowerCase().includes(lowerQuery)
  );
}

// Search clergy
export function searchClergy(query: string): Clergy[] {
  const lowerQuery = query.toLowerCase();
  return clergy.filter(c =>
    c.name.toLowerCase().includes(lowerQuery) ||
    (c.title?.toLowerCase().includes(lowerQuery) ?? false) ||
    (c.parish?.toLowerCase().includes(lowerQuery) ?? false) ||
    (c.location?.toLowerCase().includes(lowerQuery) ?? false)
  );
}

